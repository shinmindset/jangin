package construction.constructionVO;

public class ConstructionVO {
	private int conCode;
	private String conName;
	private String startDate;
	private String endDate;
	private String expStartDate;
	private String expEndDate;
	
	public int getConCode() {
		return conCode;
	}
	public void setConCode(int conCode) {
		this.conCode = conCode;
	}
	public String getConName() {
		return conName;
	}
	public void setConName(String conName) {
		this.conName = conName;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getExpStartDate() {
		return expStartDate;
	}
	public void setExpStartDate(String expStartDate) {
		this.expStartDate = expStartDate;
	}
	public String getExpEndDate() {
		return expEndDate;
	}
	public void setExpEndDate(String expEndDate) {
		this.expEndDate = expEndDate;
	}
	
	
}
