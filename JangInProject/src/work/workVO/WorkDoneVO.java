package work.workVO;

public class WorkDoneVO {
	private int no;
	private int noFk;
	private int conCode;
	private String workDate;
	private String workDone;
	private String expWorkDone;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getNoFk() {
		return noFk;
	}
	public void setNoFk(int noFk) {
		this.noFk = noFk;
	}
	public int getConCode() {
		return conCode;
	}
	public void setConCode(int conCode) {
		this.conCode = conCode;
	}
	public String getWorkDate() {
		return workDate;
	}
	public void setWorkDate(String workDate) {
		this.workDate = workDate;
	}
	public String getWorkDone() {
		return workDone;
	}
	public void setWorkDone(String workDone) {
		this.workDone = workDone;
	}
	public String getExpWorkDone() {
		return expWorkDone;
	}
	public void setExpWorkDone(String expWorkDone) {
		this.expWorkDone = expWorkDone;
	}
	
	
}
