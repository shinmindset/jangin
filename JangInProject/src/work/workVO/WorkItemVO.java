package work.workVO;

public class WorkItemVO {
	private int no;
	private int noFk;
	private int conCode;
	private String workDate;
	private int itemCode;
	private int expItemCode;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getNoFk() {
		return noFk;
	}
	public void setNoFk(int noFk) {
		this.noFk = noFk;
	}
	public int getConCode() {
		return conCode;
	}
	public void setConCode(int conCode) {
		this.conCode = conCode;
	}
	public String getWorkDate() {
		return workDate;
	}
	public void setWorkDate(String workDate) {
		this.workDate = workDate;
	}
	public int getItemCode() {
		return itemCode;
	}
	public void setItemCode(int itemCode) {
		this.itemCode = itemCode;
	}
	public int getExpItemCode() {
		return expItemCode;
	}
	public void setExpItemCode(int expItemCode) {
		this.expItemCode = expItemCode;
	}
	
	
	
}
